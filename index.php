    <section id="third"><p><strong><a size="5" href="/images/head/Science/UA/PhD_and_Postdoc_studies/16551.pdf" target="_blank">Положення про відділ аспірантури та докторантури</a></strong></p>

        <p>Порядок підготовки здобувачів вищої освіти ступеня доктора філософії та доктора наук в Університеті імені
            Альфреда Нобеля здійснюється за науковими економічними та педагогічними спеціальностями.</p>
        <p>Згідно <a href="/images/head/Science/UA/PhD_and_Postdoc_studies/15235.pdf" target="_blank"><strong>Наказу
            Міністерства освіти і науки України № 389 від 8.04.2016 р.</strong></a> та на підставі рішень Ліцензійної
            комісії МОН України (протокол № 3/2 від 08 квітня 2016 року) в Університеті імені Альфреда Нобеля розширили
            провадження освітньої діяльності на третьому освітньо-науковому рівні (<strong>доктор філософії</strong>) за
            такими спеціальностями:</p>
        <ul>
            <li><strong>015 – Професійна освіта (за спеціалізаціями),</strong></li>
            <li><strong>051 – Економіка,</strong></li>
            <li><strong>073 – Менеджмент,</strong></li>
            <li><strong>076 – Підприємництво, торгівля та біржова діяльність.</strong></li>
        </ul>
        <p>Перелік спеціальностей, відкритих у докторантурі Університету імені Альфреда Нобеля, рішення Вченої ради,
            протокол № 5 від 30 червня 2016 року</p>
        <ul>
            <li><strong>015 – Професійна освіта (за спеціалізаціями),</strong></li>
            <li><strong>051 – Економіка,</strong></li>
            <li><strong>073 – Менеджмент.</strong></li>
        </ul>
        <p>Згідно з рішенням Атестаційної колегії Міністерства освіти і науки України від 30 червня 2015 року в
            Університеті імені Альфреда Нобеля створено спеціалізовану вчену раду Д 08.120.01 з правом прийняття до
            розгляду та проведення захистів дисертацій на здобуття наукового ступеня <strong>доктора </strong>(кандидата)
            економічних наук за спеціальностями:</p>
        <ul>
            <li><strong>08.00.04 – Економіка та управління підприємствами (за видами економічної діяльності) та</strong>
            </li>
            <li><strong>08.00.03 – Економіка та управління національним господарством.</strong></li>
        </ul>
        <p>09 березня 2016 року в Університеті імені Альфреда Нобеля створено спеціалізовану вчену раду К 08.120.02 з
            правом прийняття до розгляду та проведення захистів дисертацій на здобуття наукового ступеня <strong>кандидата </strong>педагогічних
            наук за спеціальністю <strong>13.00.04 – Теорія і методика професійної освіти.</strong></p>
        <p>Ефективність роботи з 2010 по 2017 роки в спеціалізованій вченій раді Д 08.120.01 в Університеті імені
            Альфреда Нобеля характеризується захищеними аспірантами, здобувачами та співробітниками 50 дисертацій, із
            них 2- докторські (21 – викладачами, 36 - аспірантами та здобувачами університету).</p>
        <p>За 2015-2017 рр. роботи спеціалізованої вченої ради К 08.120.02 було захищено <strong>вісімнадцять</strong>
            кандидатських дисертацій за спеціальністю 13.00.04 <strong>– </strong>Теорія і методика професійної
            освіти<strong>, </strong>із них (1 – викладачем, 4 – аспірантами та здобувачами університету).</p></section>
